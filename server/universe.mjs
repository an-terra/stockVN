export const DEFAULT_MIN_AVG_VOLUME = 120_000

export function avgVolumeLast(raw, n = 20) {
  const { v } = raw
  if (!v.length) return 0
  const slice = v.slice(-n)
  return slice.reduce((a, b) => a + b, 0) / slice.length
}
